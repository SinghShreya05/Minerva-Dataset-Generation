import re
from pathlib import Path
import pandas as pd

def extract():
  contents=Path("/content/drive/MyDrive/SPDX.txt").read_text()
  contents = re.split('\n\n',contents)
  result = []
  for i in contents:
    if i.lstrip().startswith('%'):
      i = i.split('\n\n')
      for j in i:
        j = j.replace("\n"," ")
        result.append(j)
  org = []
  regex = []
  iter = len(result)
  for i in range(iter):
    regex.append(result[i].split("%STR%",1)[1])
    tokens = re.findall("[\w']+", result[i])
    org.append(tokens[1])
    sen = result[i].split("%STR%",1)[0]
  df = pd.DataFrame({'Licenses':org,'Regex':regex})
  df['Licenses'] = df['Licenses'].apply(lambda x: x.replace("_SPDX_",""))
  df.to_csv('SPDX_regex.csv',index=False)

extract()

